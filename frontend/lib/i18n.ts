export function isAppLanguage(value: string | null | undefined): value is AppLanguage {
  return value === "en" || value === "uz" || value === "ru";
}

export function resolveTranslationValue(language: AppLanguage, key: string): unknown {
  const parts = key.split(".");
  let current: unknown = TRANSLATIONS[language];
  for (const part of parts) {
    if (!current || typeof current !== "object" || !(part in (current as Record<string, unknown>))) {
      current = undefined;
      break;
    }
    current = (current as Record<string, unknown>)[part];
  }

  if (current === undefined && language !== DEFAULT_LANGUAGE) {
    current = resolveTranslationValue(DEFAULT_LANGUAGE, key);
  }
  return current;
}

export function interpolateTranslation(template: string, values?: Record<string, string | number>): string {
  if (!values) return template;
  return template.replace(/\{\{\s*(\w+)\s*\}\}/g, (_, key: string) =>
    values[key] === undefined ? "" : String(values[key]),
  );
}
